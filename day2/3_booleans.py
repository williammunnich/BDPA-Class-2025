"""
Booleans
- Explain `True` and `False`
- Example: Checking if a detective has solved a case
"""
#A boolean is a data type that can only be `True` or `False`
# It is often used to represent a binary state or condition.

# Define a variable for if the case is solved
case_solved = True

# Print the status of the case
print("Has Detective Smith solved the case?")
print(case_solved)

# Check if case is solved using if/else
if case_solved:
    print("Great work Detective! Case closed.")
else:
    print("Keep investigating, Detective.")

