# Detective's age in years
age = 45

# Years of experience (assuming started at age 25)
experience = age - 25

# Success rate as a percentage (floating point)
success_rate = 92.5

# Cases solved per year (integer)
cases_per_year = 24

# Total cases solved
total_cases = experience * cases_per_year

print("Detective's experience:", experience, "years")
print("Success rate:", success_rate, "%")
print("Total cases solved:", total_cases)

# Demonstrating arithmetic operations
print("\nArithmetic examples:")
print("Addition: 5 + 3 =", 5 + 3) # Adding two integers
print("Subtraction: 10 - 4 =", 10 - 4) # Subtracting two integers
print("Multiplication: 6 * 2 =", 6 * 2) # Multiplying two integers
print("Division: 15 / 3 =", 15 / 3) # Dividing two integers (gives a decimal result)
print("Integer division: 17 // 5 =", 17 // 5) # Integer division (no remainder or decimal)
print("Modulus: 17 % 5 =", 17 % 5) # Remainder of division
print("Exponents: 2 ** 3 =", 2 ** 3) # 2 raised to the power of 3

